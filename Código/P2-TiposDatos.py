# Alfanumérico (string)
cadena = "Hola, este es un string."
print("String:", cadena)

# Enteros
entero = 42
print("Entero:", entero)

# Flotantes
flotante = 3.14159
print("Flotante:", flotante)

# Binarios
binario = 0b1010  # Número binario representado como entero
print("Binario (como entero):", binario)
print("Binario (formato binario):", bin(binario))

# Listas
lista = [1, 2, 3, "texto", 4.5, 0b110]
print("Lista:", lista)
