def mostrar_tipos_de_datos():
    # Ejemplo de tipo entero (int)
    numero_entero = 42
    print(f"Entero (int): {numero_entero} - Tipo: {type(numero_entero)}")

    # Ejemplo de tipo flotante (float)
    numero_flotante = 3.14
    print(f"Flotante (float): {numero_flotante} - Tipo: {type(numero_flotante)}")

    # Ejemplo de tipo cadena de texto (str)
    texto = "¡Hola, GitHub!"
    print(f"Cadena de texto (str): {texto} - Tipo: {type(texto)}")

    # Ejemplo de tipo booleano (bool)
    valor_booleano = True
    print(f"Booleano (bool): {valor_booleano} - Tipo: {type(valor_booleano)}")

    # Ejemplo de tipo lista (list)
    lista = [1, 2, 3, "cuatro", 5.5]
    print(f"Lista (list): {lista} - Tipo: {type(lista)}")

    # Ejemplo de tipo tupla (tuple)
    tupla = (10, 20, 30, "cuarenta")
    print(f"Tupla (tuple): {tupla} - Tipo: {type(tupla)}")

    # Ejemplo de tipo conjunto (set)
    conjunto = {1, 2, 3, 3, 4}
    print(f"Conjunto (set): {conjunto} - Tipo: {type(conjunto)}")

    # Ejemplo de tipo diccionario (dict)
    diccionario = {"nombre": "GitHub", "año": 2008, "lenguaje": "Python"}
    print(f"Diccionario (dict): {diccionario} - Tipo: {type(diccionario)}")

    # Ejemplo de tipo None
    valor_nulo = None
    print(f"Nulo (NoneType): {valor_nulo} - Tipo: {type(valor_nulo)}")

# Ejecutar la función
mostrar_tipos_de_datos()
