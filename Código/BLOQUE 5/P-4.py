# Definición de la función
def saludar(nombre): # nombre de un parámetro
    print(f"\n ¡Hola, {nombre}!")
    print("¿Cómo estas?")
    print("¡Espero que te encuentres bien! \n")

# Llamar la función
saludar("Edinguer")  # Edinguer es un argumento
saludar("Chuyito")   # Chuyito es un argumento
saludar("terrícola") # terrícola es un argumento