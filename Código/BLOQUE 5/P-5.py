# Definición de la función
def saludar(nombre, edad): # nombre y edad son parámetros
    print(f"\n ¡Hola, {nombre}!")
    print(f"Tienes {edad} años.")
    print("¡Espero que te encuentres bien! \n")
    

# Llamar la función
saludar("Edinguer",26)  # Edinguer y 26 son argumentos
saludar("Chuyito", 18)   # Chuyito y 18 son argumentos
saludar("terrícola", 23) # terrícola y 23 son argumentos