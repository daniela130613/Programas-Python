# ==, !=, >=, <=, >,<

print ("-*-*- OPERADOR IGUAL (==) -*-*-")
print( 5 == 5 ) # TRUE
print( 10 == 7 ) # FALSE
print( 5.0 == 5 ) # TRUE

print("\n")

print ("-*-*- OPERADOR DIFERENTE o NO IGUAL (!=) -*-*-") 
print( 5 != 5 ) # FALSE
print( 10 != 7 ) # TRUE
print( 5.0 != 5 ) # FALSE

print("\n")

print ("-*-*-OPERADOR MAYOR QUE (>)-*-*-")
print( 10 > 5 ) # TRUE
print( 5 > 10 ) # FALSE

print("\n")

print ("-*-*-OPERADOR MENOR QUE (<)-*-*-")
print( 5 < 10 ) # TRUE
print( 10 < 5 ) # FALSE

print("\n")

print ("-*-*- OPERADOR MAYOR O IGUAL QUE  (>=) -*-*-")
print( 5 >= 5 ) # TRUE
print( 10 >= 7 ) # TRUE
print( 3 >= 5 ) # FALSE

print("\n")

print ("-*-*-OPERADOR MENOR QUE O IGUAL QUE (<=) -*-*-")
print( 5 <= 5 ) # TRUE
print( 10 <= 7 ) # FALSE
print( 3 <= 5 ) # TRUE
